
class User {
  final String name;
  final String email;
  final String image;
  final String region;
  final String mobile;
  final String zone;
  final bool isOnline;

  User({
    required this.name,
    required this.email,
    required this.image,
    required this.region,
    required this.mobile,
    required this.zone,
    required this.isOnline,
  });
}
